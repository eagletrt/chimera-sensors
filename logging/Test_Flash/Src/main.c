/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  data_stc data={.ver=10, .max1=100, .max2=200, .min1=10, .min2=20};

  uint8_t ver;
  uint16_t flag = 0;

  //UNUSED METHODS TO ACQUIRE DATA FROM MEMORY
  //buff = *((uint8_t*) &userConfig[0]);
  //memcpy(&txt_, &userConfig[0], 8);

  //reading in the first address of the memory
  ver = userConfig[0];
  if(ver == data.ver){						//if the byte is equal to our ver, we can read the next saved data
	  flag = 1;
  }
  else{										//else we have to write others data

	  HAL_FLASH_Unlock();					//unlocking the flash memory

	  __HAL_FLASH_CLEAR_FLAG(FLASH_FLAG_EOP | FLASH_FLAG_OPERR | FLASH_FLAG_WRPERR | FLASH_FLAG_PGAERR | FLASH_FLAG_PGSERR );

	  FLASH_Erase_Sector(FLASH_SECTOR_7, VOLTAGE_RANGE_3);

	  int error = HAL_FLASH_Program(TYPEPROGRAM_BYTE, &userConfig[0], 10);		//write byte

	  uint8_t h = data.max1 / 256;
	  uint8_t l = data.max1 % 256;

	  error = HAL_FLASH_Program(TYPEPROGRAM_BYTE, &userConfig[1], h);
	  error = HAL_FLASH_Program(TYPEPROGRAM_BYTE, &userConfig[2], l);

	  HAL_FLASH_Lock();						//unlocking the flash memory
  }

  while (1)
  {

  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
	  uint8_t txt[10];
	  txt[0] = userConfig[0];
	  txt[1] = userConfig[1];
	  txt[2] = userConfig[2];
	  //sprintf(txt, "%d\t%d\r\n", error, &userConfig[0]);
	  char text[4];
	  sprintf(text, "%d\t%d\t-", flag, ver);
	  HAL_UART_Transmit(&huart2, (uint8_t*)text, strlen(text), 10);
	  //HAL_UART_Transmit(&huart2, (uint8_t*)txt_, 1, 10);
	  HAL_UART_Transmit(&huart2, (uint8_t*)txt, 10, 10);
	  HAL_UART_Transmit(&huart2, (uint8_t*)"\r\n", 2, 10);

  }
  /* USER CODE END 3 */

}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;

    /**Configure the main internal regulator output voltage 
    */
  __HAL_RCC_PWR_CLK_ENABLE();

  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_NONE;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_HSI;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV1;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_0) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

