/* USER CODE BEGIN 4 */
void HAL_CAN_RxFifo0FullCallback(CAN_HandleTypeDef *hcan){
	uint8_t dataRx[8];
	int id = CAN_Receive(dataRx);

	can_id.total_msg ++;

	if(id == 0xD0){

		switch(dataRx[0]){
		case 0x06:
			sensor.encoder ++;
			break;
		case 0x07:
			sensor.gps_latitude ++;
			break;
		case 0x08:
			sensor.gps_longitude ++;
			break;
		}

	}

	if(id == 0xC0){
		switch(dataRx[0]){
		case 0x02:
			sensor.steer_angle ++;
			break;
		case 0x04:
			sensor.gyro ++;
			break;
		case 0x05:
			sensor.acc ++;
			break;
		}
	}

	if(id == 0xB0){
		switch(dataRx[0]){
		case 0x01:
			sensor.apps ++;
			break;
		case 0x02:
			sensor.brk ++;
			break;
		}
	}

}

int CAN_Receive(uint8_t * dataRx){

	CAN_RxHeaderTypeDef RxHeader;

	if (HAL_CAN_GetRxFifoFillLevel(&hcan1, CAN_RX_FIFO0) != 0){
		HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &RxHeader, dataRx);
	}

	int id = RxHeader.StdId;

	return id;
}

